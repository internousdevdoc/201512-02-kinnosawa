drop database if exists kinnosawa;
create database kinnosawa;
use kinnosawa;

create table user(
id int primary key not null auto_increment,
email varchar(100) unique,
password varchar(16),
name varchar(100),
name_kana varchar(50),
tel_num varchar(11),
postal_code varchar(7),
address  varchar(255),
card_num varchar(4),
token varchar(255),
oauth_id varchar(50),
oauth_name varchar(15),
created_at datetime not null default current_timestamp,
updated_at datetime not null default current_timestamp on update current_timestamp
);

create table goods(
id int primary key not null auto_increment,
name varchar(100) not null,
category varchar(10) not null,
close_up boolean not null default 0,
price float not null,
detail varchar(255) not null,
img_path varchar(100) not null,
set_img_path varchar(100) not null,
allergie varchar(255) not null,
top_page boolean not null,
created_at datetime not null default current_timestamp,
updated_at datetime not null default current_timestamp on update current_timestamp
);

create table send_address(
id int primary key not null auto_increment,
user_id int not null,
send_name varchar(100) not null,
postal_code varchar(7),
tel_num varchar(11) not null,
address varchar(255) not null,
created_at datetime not null default current_timestamp,
foreign key(user_id) references user(id)
);

create table order_history(
id int primary key not null auto_increment,
order_number int not null,
user_id int not null,
send_id int not null,
goods_id int not null,
number int not null,
created_at datetime not null default current_timestamp,
foreign key(user_id)references user(id),
foreign key(send_id)references send_address(id),
foreign key(goods_id)references goods(id)
);

create table cart(
user_id int not null,
goods_id int not null,
number int not null,
created_at datetime not null default current_timestamp,
updated_at datetime not null default current_timestamp on update current_timestamp,
foreign key(user_id) references user(id),
foreign key(goods_id) references goods(id)
);

create table admin(
id int primary key not null auto_increment,
password varchar(255) not null,
name varchar(50) unique not null,
created_at datetime not null default current_timestamp,
updated_at datetime not null default current_timestamp on update current_timestamp
);
